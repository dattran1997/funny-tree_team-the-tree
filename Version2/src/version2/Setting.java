    public void next(){
        try{
            PanelMain.removeAll();
        }catch(Exception ex){}
            Home home = new Home();
            home.setBounds(0, 0, 450, 600);
            home.View(NAMELOGIN);
            PanelMain.add(home);
            PanelMain.repaint();
            home.start();
    }
    public void setup(){
        try{
            PanelMain.removeAll();
        }catch(Exception ex){}
            Setup setup = new Setup();
            setup.setBounds(0, 0, 450, 600);
            PanelMain.add(setup);
            PanelMain.repaint();
            setup.start();
    }
    public void logout(){
        try{
            PanelMain.removeAll();
        }catch(Exception ex){}
            Login login = new Login();
            login.setBounds(0, 0, 450, 600);
            PanelMain.add(login);
            PanelMain.repaint();
            login.start();
    }

